            <p class="acordeon__contenido">La educación de calidad es la única herramienta de política pública que ha probado ser efectiva en el mejoramiento de la movilidad social y en la reducción de brechas y desigualdades entre distintos grupos de personas en el interior de un país.</p>

            <p class="acordeon__contenido">Por lo mismo, Bogotá es referente de la anterior premisa con un esfuerzo fiscal de 1,1 billones de pesos para la construcción de 64 colegios (30 colegios nuevos y 34 edificios nuevos en colegios existentes), de los cuales a la fecha se han entregado 27, 19 están en obra, 12 en diseño y seis en gestión predial.</p>

            <p class="acordeon__contenido">El colegio Rogelio Salmona de Ciudad Bolívar es uno de los nuevos estandartes en materia de infraestructura y modelo educativo en Bogotá. Es una megaestructura completamente nueva, que tuvo una inversión de 34.618 millones, de los cuales la Alcaldía de Bogotá aportó 24.370 millones de pesos y el gobierno nacional 9.888 millones de pesos.</p>
