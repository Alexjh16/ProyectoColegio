            <p class="acordeon__contenido">Tenga en cuenta lo siguiente:</p>

            <p class="acordeon__contenido">1. El proceso de matrículas es gratuito y se hace directamente a través de la página www.educacionbogota.edu.co. No se requieren intermediarios ni retribuciones económicas de ningún tipo.</p>

            <p class="acordeon__contenido">2. Al momento de la inscripción, los padres o acudientes deben tener disponible su documento de identidad y el del estudiante, así como un recibo de servicio público del lugar de residencia, que debe ser adjuntado al formulario durante el proceso de inscripción. Es importante registrar el correo electrónico y el número de celular, a través de los cuales serán informados del desarrollo del proceso. </p>

            <p class="acordeon__contenido">3. La formalización de la matrícula es indispensable para finalizar el proceso. Es fundamental que los padres o acudientes asistan a la cita asignada para tal fin el día y hora establecidos. </p>

            <p class="acordeon__contenido">4. Si los estudiantes son asignados en una institución educativa ubicada lejos de su lugar de residencia, los padres podrán solicitar cupo y beneficio de movilidad escolar, previo cumplimiento de requisitos.</p>

            <p class="acordeon__contenido">5. Matricularse es muy fácil:</p>

